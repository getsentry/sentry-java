package io.sentry.samples.spring.boot;

public class MyBean {

  public MyBean() {}

  public MyBean(Integer id, String name) {
    this.id = id;
    this.name = name;
  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  Integer id;
  String name;
}
